public class ThreeDimensionalArrays_learn {
    public static void main(String[] args) {


        int[][][] arr = {{{1, 2}, {3, 4}}, {{5, 6}, {7, 8}}};

        System.out.println("arr[0][0][0] = " + arr[0][0][0]);
        System.out.println("arr[1][0][0] = " + arr[1][0][0]);
        System.out.println("arr[0][1][0] = " + arr[0][1][0]);
        System.out.println("arr[1][1][0] = " + arr[1][1][0]);
        System.out.println("arr[0][0][1] = " + arr[0][0][1]);
        System.out.println("arr[1][0][1] = " + arr[1][0][1]);
        System.out.println("arr[0][1][1] = " + arr[0][1][1]);
        System.out.println("arr[1][1][1] = " + arr[1][1][1]);



    }
}
